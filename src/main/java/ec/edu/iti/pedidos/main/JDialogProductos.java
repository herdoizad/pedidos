    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        // TODO add your handling code here:
        if (jTextNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar un producto", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int opcion = JOptionPane.showConfirmDialog(rootPane, "Desea eliminar el producto?", "Eliminar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (opcion == JOptionPane.YES_OPTION) {
            Producto producto = new Producto();
            producto.setProCodigo(codigo);
            producto.setProNombre(jTextNombre.getText());
            producto.setProPrecio(new BigDecimal(jTextPrecio.getText()));
            productoDAO.eliminar(producto);
            JOptionPane.showMessageDialog(rootPane, "Producto eliminado corectamente", "Información", JOptionPane.INFORMATION_MESSAGE);
            cargarDatos();
            deshabilitar();
            limpiar();
        }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        // TODO add your handling code here:
        if (jTextBuscar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Ingrese un nombre paras buscar", "Error", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        List<Producto> listaProductos = productoDAO.buscarPorNombre(jTextBuscar.getText());
        modeloTabla.setRowCount(0);

        for (Producto p : listaProductos) {
            modeloTabla.addRow(new Object[]{
                p.getProCodigo(),
                p.getProNombre(),
                p.getProPrecio()});
        }

    }//GEN-LAST:event_jButtonBuscarActionPerformed

