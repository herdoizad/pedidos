    private void jButtonGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGrabarActionPerformed
        // TODO add your handling code here:
        if (!validarDatos()) {
            return;
        }

        Producto producto = new Producto();

        try {
            if (nuevo) {
                producto.setProNombre(nombre);
                producto.setProPrecio(new BigDecimal(precio));
                productoDAO.insertar(producto);
                JOptionPane.showMessageDialog(rootPane, "Producto agregado correctamente", "Información", JOptionPane.INFORMATION_MESSAGE);
            } else {
                producto.setProCodigo(codigo);
                producto.setProNombre(nombre);
                producto.setProPrecio(new BigDecimal(precio));
                productoDAO.actualizar(producto);
                JOptionPane.showMessageDialog(rootPane, "Producto actualizado correctamente", "Información", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Error al grabr producto", "Error", JOptionPane.ERROR_MESSAGE);
            System.out.println("Error al grabar producto " + ex);
        }

        cargarDatos();
        deshabilitar();
        limpiar();
    }//GEN-LAST:event_jButtonGrabarActionPerformed

