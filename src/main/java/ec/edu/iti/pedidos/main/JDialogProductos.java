    /**
     * Método para mostrar los datos de productos
     */
    public void cargarDatos() {
        List<Producto> listaProductos = productoDAO.listarTodo();
        modeloTabla.setRowCount(0);

        for (Producto p : listaProductos) {
            modeloTabla.addRow(new Object[]{
                p.getProCodigo(),
                p.getProNombre(),
                p.getProPrecio()});
        }
    }

    /**
     * Método para validar datos
     *
     * @return TURE si los datos son válidos
     */
    private boolean validarDatos() {
        nombre = jTextNombre.getText();
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar un nombre válido", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        try {
            precio = Double.parseDouble(jTextPrecio.getText());
            if (precio <= 0) {
                JOptionPane.showMessageDialog(rootPane, "Debe ingresar un valor myor a cero", "Error", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar un precio válido", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
    }

    /**
     * Método para cargar datos del producto desde la tabla
     */
    private void cargarDatosTabla() {
        int fila = jTableProductos.getSelectedRow();
        if (fila >= 0) {
            codigo = Integer.parseInt(jTableProductos.getValueAt(fila, 0).toString());
            jTextNombre.setText(jTableProductos.getValueAt(fila, 1).toString());
            jTextPrecio.setText(jTableProductos.getValueAt(fila, 2).toString());
        }
    }

